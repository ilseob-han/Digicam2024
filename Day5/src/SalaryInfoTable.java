
public class SalaryInfoTable {
	
	private double salaryDayWork;
	private double salaryNightWork;
	private double salaryHolidayWork;
	
	SalaryInfoTable () {

	}

	public double getSalaryDayWork() {
		return salaryDayWork;
	}

	public void setSalaryDayWork(double salaryDayWork) {
		this.salaryDayWork = salaryDayWork;
	}

	public double getSalaryNightWork() {
		return salaryNightWork;
	}

	public void setSalaryNightWork(double salaryNightWork) {
		this.salaryNightWork = salaryNightWork;
	}

	public double getSalaryHolidayWork() {
		return salaryHolidayWork;
	}

	public void setSalaryHolidayWork(double salaryHolidayWork) {
		this.salaryHolidayWork = salaryHolidayWork;
	}
	
}
